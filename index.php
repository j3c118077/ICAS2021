            <h2>Welcome to International Conference on Applied Science  (ICAS) 2021</h2>
            <p>
            Corona virus outbreak or better known as COVID-19 has been determined not only by Indonesia, 
            but also other countries in various parts of the world. 
            WHO as the world health organization has also established this outbreak as a global pandemic. 
            The COVID-19 epidemic has such a wide distribution with a high mortality rate. COVID-19 pandemic 
            has generated an impact on the changing order of various fields of life. Not only in the field of 
            health, but also economic, social, cultural, and education. </p>
            <p>
            In the higher education sector, the COVID-19 outbreak raises a 
            polemic about how the role of higher education in tackling the 
            impact of the COVID-19 outbreak. Higher education is morally demanded 
            to present a solution to the multiplier effect of Covid-19 during the 
            pandemic period or even after the pandemic has passed (new normal era). 
            Therefore creativity and innovation are needed by the role of universities 
            and the academic community in accordance with their respective fields in 
            answering all the challenges that exist.</p>
            <p>
            College of Vocational Studies, IPB University as part of a vocational college, 
            carries the Agro-Maritime 4.0 transformation in accordance with the IPB University 
            roadmap. The concept of agro-maritime 4.0 exists to overcome the issues of the 
            disconnectivity of agro-maritime development, environmental degradation and natural 
            resources, low welfare of the people, food insecurity, low contribution of the 
            agro-maritime sector to economic growth, and inequality of development between regions 
            in Indonesia. The scope of Agro-Maritime 4.0 covers the development, improvement of the 
            quality and quantity of production and marketing facilities in a broad agricultural and 
            marine system. The concept of Agro-Maritime 4.0 transformation in the new normal era is 
            important to get an overview of its implementation, considering Agro-Maritime 4.0 is 
            related to how to produce production inputs, commodities and products that are the basic
             needs of humans in the new normal era.
            </p>
            <p>
            International conference activities on applied science need to be held as a means of 
            socializing and communicating the needs and implementation of applied technology and 
            its sustainability in the Agro-Maritime 4.0 sector in the new normal era and going forward. 
            It is expected that the results of the conference will obtain various ideas and inputs 
            related to the development of technology and applied applications of Agro-Maritime 4.0 
            and its sustainability. The conference will bring experts from other countries to obtain 
            information about the implementation of applied technology and its sustainability in the 
            current Agro-Maritime sector in other countries. This forum is also intended to exchange ideas, 
            information and knowledge among global stakeholders.</p>
