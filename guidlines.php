              <li><b>Type of Articles</b></li>
              <ul>
                  <li><b>Research article</b> includes original research paper in quantitative, qualitative or both.</li>
                  <li><b>Review</b> refers to systematic review, literature review, and topical issues of interest related to health and applied science in health.</li>
                  <li><b>Best practice</b> highlights real-world innovative public health programs and activities, including evaluation of existing health and applied science programs.</li>
                  <li><b>Photo essays</b> mostly comprise of 10-15 high resolution photographs with 200-500 words of text on a topic that relates to health and well-being.  The photographs can broadly describe the extension of political, social, and environmental determinants of health. Authors hold the right to reproduce the photographs and must able to provide GLOBAL HEALTH MANAGEMENT JOURNAL (GMHJ) with permission to reproduce.</li>
              </ul>

              <b><li>Manuscript Preparation</li></b>
              <ol>
                <li>Manuscripts should be typewritten in English with 2.5 cm (1 inch) margins all around, 11-point Times New Roman font, and 1.5 line-spacing with a maximum 3000 words.</li>
                <li>Manuscript should include these following sections: </li>
                <ul>
                  <li><b>Title and list of authors</b>. The title must be concise, clear, and informative. Titles with more than 100 characters are not prohibited, but they are discouraged. All authors should be listed using first name, initials, last name and academic affiliation. The corresponding author should be specified, and an address for correspondence (usually an e-mail address) should be given.</li>
                  <li><b>Abstract and Keywords</b>. 
                  <ol>
                      <li><b>Abstract</b> must be written in English, single spaced and not exceeding 300 words. Abstract must be in text only (no graphs, tables, diagrams, images, etc.). Abstract should include justification of the study, aims, methodology, results and conclusion.</li>
                      <li><b>Keywords</b>: Provide at least three keywords to enable online search.</li>
                  </ol>
                  <li><b>Content</b> consists of Introduction, Methods, Results, Discussion, Conclusion, Conflict of Interest, Acknowledgement (if any), and References. </li>
                  <ol>
                      <li><b>Introduction section</b> should present the context and justification of the research, the knowledge gap that the research addresses, and the research hypothesis as appropriate.</li>
                      <li><b>Methods section</b>. This section should clearly present research procedures, sample size calculations as appropriate, dependent and independent variables, comparisons made in testing research hypotheses, and statistical techniques applied.Null hypotheses may be stated in this section. </li>
                      <li><b>Results section</b> should clearly present study findings, using text, tables, and figures as appropriate. This section usually includes descriptive findings (e.g., distributions of dependent and independent variables) and analytical findings (e.g., associations between dependent and independent variables).</li>
                      <li><b>Discussion section</b> presents interpretation of research findings, relates findings to findings of relevant previous research, summarizes study strengths and limitations, and makes research-related and policy-related recommendations as appropriate.</li>
                      <li><b>Conclusion section</b> should covers major findings of the study, and policy recommendations.</li>
                      <li>In the manuscript, authors must declare their potential Conflict of Interest as a result of financial relationships (such as employment, consultancies, stock ownership, honoraria, patents, and paid expert testimony)</li>
                        <li>If any, <b>Acknowledgment</b> of persons, organizations, and funding sources, should be placed before the reference section. </li>
                        </ol>
                </ul>
              </ol>
              <br>
              <b><li>Citation and References</li></b>
              <p>The Vancouver style reference format should be followed. In-text citations are to be numbered consecutively in parentheses.  In the reference list, citations should be given in the same numbered order as in the text.  All authors should be quoted for papers with up to six authors, and for papers with more than six authors, the first six should be quoted followed by "et al." Periodical (Journal) abbreviations should follow those used by PubMed (http://www.ncbi.nlm.nih.gov/nlmcatalog/journals).  Some examples of how to quote the references are given below.  </p>
